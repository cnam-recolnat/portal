#!/bin/bash
#-----------------------------------------------------------8<----------------------------------------------------------
haproxy -vv
haproxy -f /root/haproxy.cfg
#-----------------------------------------------------------8<----------------------------------------------------------
cd etcd-v2.0.0-rc.1-linux-amd64
nohup ./etcd &
./etcd --version
