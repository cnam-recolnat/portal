#!/bin/bash
#-----------------------------------------------------------8<----------------------------------------------------------
ip=$(ifconfig eth0 | grep 'inet addr:' | cut -d: -f2 | awk '{ print $1}')
echo $ip
#-----------------------------------------------------------8<----------------------------------------------------------
rm -rf /data
#-----------------------------------------------------------8<----------------------------------------------------------
echo "Starting consul agent"
nohup consul agent 						\
					-config-dir=/config \
					-server 			\
					-bootstrap
#-----------------------------------------------------------8<----------------------------------------------------------
echo "root:abracadabra" | chpasswd
#-----------------------------------------------------------8<----------------------------------------------------------
exec /usr/sbin/sshd -D
